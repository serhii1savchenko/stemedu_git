/**
 * Polynomial term. Powers are stored in reverted lexicographical order.
 * E.g. term {@code z^3yx^2} from {@code Ring=Z[x, y, z]} ({@code z > y > x})
 * is stored as {@code [2, 1, 3]}.
 */
public class Term extends Element {
    public static final long serialVersionUID = 1L;

    private final int[] powers;
    public static final Term ZERO = new Term();

    private Term() {
        powers = new int[] {};
    }

    public Term(int... powers) {
        this.powers = copyOf(powers, powers.length);
    }

    public int size() {
        return powers.length;
    }

    public Term multiply(Term p) {
        int[] res;
        int[] otherPowers = p.powers();
        int otherLength = p.size();
        int length = size();
        if (length > otherLength) {
            res = new int[length];
            for (int i = 0; i < otherLength; i++) {
                res[i] = powers[i] + otherPowers[i];
            }
            System.arraycopy(powers, otherLength, res, otherLength, length - otherLength);
        } else {
            res = new int[otherLength];
            for (int i = 0; i < length; i++) {
                res[i] = powers[i] + otherPowers[i];
            }
            System.arraycopy(otherPowers, length, res, length, otherLength - length);
        }
        return new Term(res);
    }

    /**
     * Multiplies this term by given polynomial.
     *
     * @param p polynom.
     *
     * @return
     */
    public Polynom multiply(Polynom p) {
        int polynomVarsNum = p.getVarsNum();
        int size = size();
        int[] resPowers;
        if (polynomVarsNum >= size) {
            resPowers = new int[p.powers.length];
            for (int i = 0; i < p.coeffs.length; i++) {
                for (int j = 0; j < size; j++) {
                    resPowers[i * polynomVarsNum + j]
                            = p.powers[i * polynomVarsNum + j] + powers[j];
                }
            }
        } else {
            resPowers = new int[p.coeffs.length * size];
            for (int i = 0; i < p.coeffs.length; i++) {
                for (int j = 0; j < polynomVarsNum; j++) {
                    resPowers[i * size + j]
                            = p.powers[i * polynomVarsNum + j] + powers[j];
                }
                System.arraycopy(powers, polynomVarsNum,
                        resPowers, i * size + polynomVarsNum,
                        size - polynomVarsNum);
            }
        }
        return new Polynom(resPowers, p.coeffs);
    }

    /**
     * Divides this term by given term. It's assumed that size of this term is
     * greater or equal to size of given term.
     *
     * @param t term.
     *
     * @return term = this / t.
     */
    public Term div(Term t) {
        int len = size();
        int otherLen = t.size();
        int[] otherPowers = t.powers();
        int[] resPowers = new int[len];
        System.arraycopy(powers, 0, resPowers, 0, len);
        for (int i = 0; i < otherLen; i++) {
            resPowers[i] -= otherPowers[i];
        }
        return new Term(resPowers);
    }

    /**
     * Trims zeroes at the end of powers array (unused highest powers).
     *
     * @return trimmed copy of this term.
     */
    public Term trim() {
        int resLen = size();
        for (int i = resLen - 1; i >= 0; i--) {
            if (powers[i] == 0) {
                resLen--;
            } else {
                break;
            }
        }
        int res[] = new int[resLen];
        System.arraycopy(powers, 0, res, 0, resLen);
        return new Term(res);
    }

    /**
     * @param t
     *
     * @return least common multiplier of this and given terms.
     */
    public Term lcm(Term t) {
        int len = size();
        int otherLen = t.size();
        int[] otherPowers = t.powers();
        int[] resPowers = new int[Math.max(len, otherLen)];
        for (int i = 0; i < Math.min(this.size(), t.size()); i++) {
            resPowers[i] = (powers[i] > otherPowers[i]) ? powers[i] : otherPowers[i];
        }
        if (Math.max(len, otherLen) == len) {
            System.arraycopy(powers, Math.min(len, otherLen), resPowers,
                    Math.min(len, otherLen), resPowers.length - Math.min(len, otherLen));
        } else {
            System.arraycopy(otherPowers, Math.min(len, otherLen), resPowers,
                    Math.min(len, otherLen), resPowers.length - Math.min(len, otherLen));
        }
        return new Term(resPowers);
    }

    /**
     * @param t
     *
     * @return true, if this terms is reducible by given term, false otherwise.
     */
    public boolean isReducibleBy(Term t) {
        return t.lcm(this).equals(this);
    }

    /**
     * @param p
     *
     * @return
     */
    public boolean isReducibleBy(Polynom p) {
        return this.isReducibleBy(p.hTerm());
    }

    /**
     * @return true if this term contains only zero powers, false otherwise.
     */
    public boolean hasZeroPowers() {
        for (int i = 0, size = size(); i < size; i++) {
            if (powers[i] != 0) {
                return false;
            }
        }
        return true;
    }

    public Polynom toPolynom(Ring ring) {
        return new Polynom(powers(), new Element[] {ring.numberONE});
    }

    public Polynom toPolynom() {
        return new Polynom(powers(), new Element[] {Ring.ringZxyz.numberONE});
    }

    /**
     * @return total degree (sum of powers) of this term.
     */
    public int deg() {
        int res = 0;
        for (int i = 0, size = size(); i < size; i++) {
            res += powers[i];
        }
        return res;
    }

    @Override
    public String toString() {
        return this.toPolynom().toString();
    }

    @Override
    public String toString(Ring ring) {
        return this.toPolynom(ring).toString(ring);
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 19 * hash + Arrays.hashCode(powers);
        return hash;
    }

    @Override
    public boolean equals(Object o) {
        if (o == this) {
            return true;
        }
        if (!(o instanceof Term)) {
            return false;
        }
        Term t = (Term) o;
        int[] otherPowers = t.powers();
        int len = size();
        if (t.size() != len) {
            return false;
        }
        for (int i = 0; i < len; i++) {
            if (otherPowers[i] != powers[i]) {
                return false;
            }
        }
        return true;
    }

    public int compareTo(Term other) {
        int len = size();
        int otherLen = other.size();
        int[] otherPowers = other.powers();
        if (len > otherLen) {
            return 1;
        }
        if (len < otherLen) {
            return -1;
        }
        for (int i = otherLen - 1; i >= 0; i--) {
            if (powers[i] > otherPowers[i]) {
                return 1;
            }
            if (powers[i] < otherPowers[i]) {
                return -1;
            }
        }
        return 0;
    }

    @Override
    public int compareTo(Element x, Ring ring) {
        if (x instanceof Term) {
            return compareTo((Term) x);
        }
        return -1;
    }

    @Override
    public int compareTo(Element o) {
        if (o instanceof Term) {
            return compareTo((Term) o);
        }
        return -1;
    }

    @Override
    public Boolean isZero(Ring ring) {
        return powers.length == 0;
    }

    @Override
    public boolean equals(Element x, Ring r) {
        if (x instanceof Term) {
            return equals(x);
        }
        return false;
    }

    @Override
    public Boolean isOne(Ring ring) {
        throw new UnsupportedOperationException("Not supported yet.");
    }

    /**
     * @return array of powers of this term.
     */
    public int[] powers() {
        return Array.copyOf(powers, powers.length);
    }
}
